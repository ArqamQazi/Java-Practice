//Write a program to print the multiplication table of a given number n.
package BaiscPractice.Chapter5;

public class Q3 {
    public static void main(String[] args) {
        int n = 2;
        for (int i = 1; i <= 10; i++) {
            System.out.println(n + " x " + i + " = " + n * i);
        }
    }
}
